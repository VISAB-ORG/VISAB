package org.visab.processing.cbrshooter.model;

public class PlayerStatistics {

    private int deaths;
    private int frags;

    public int getDeaths() {
	return deaths;
    }

    public int getFrags() {
	return frags;
    }

    public void setDeaths(int deaths) {
	this.deaths = deaths;
    }

    public void setFrags(int frags) {
	this.frags = frags;
    }
}
